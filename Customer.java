public class Customer{
    public String username;
    public String name;
    public char state;
    public int phone_num;
    public String email;
    public int tax_id;
    public String password;

    public Customer(String username, String name, char state, int phone_num, String email, int tax_id, String password) {
        this.username = username;
        this.name = name;
        this.state = state;
        this.phone_num = phone_num;
        this.email = email;
        this.tax_id = tax_id;
        this.password = password;


    }
    
}